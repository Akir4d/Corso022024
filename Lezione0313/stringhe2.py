
x = input ("Inserisci una stringa:")

# split consente di trasfomare la stringa in una lista si valori
# in questo caso si e' scelto la virgola ma avremmo potuto usare pure gli spazi
print (x.split(sep=","))

# upper rende tutto maiuscolo
print (x.upper())

y = "Python e' fantastico!"
# le stringhe si uniscono con +
print (x + " " + y)

